parasails.registerPage('invite', {
  //  ╦╔╗╔╦╔╦╗╦╔═╗╦    ╔═╗╔╦╗╔═╗╔╦╗╔═╗
  //  ║║║║║ ║ ║╠═╣║    ╚═╗ ║ ╠═╣ ║ ║╣
  //  ╩╝╚╝╩ ╩ ╩╩ ╩╩═╝  ╚═╝ ╩ ╩ ╩ ╩ ╚═╝
  data: {

    formData: {
      secondsPerSide: 10,
      increment: 3,
      pieces: 'random',
      username: '',
    },

    formErrors: {},

    formRules: {
      username: { required: true },
    },

    syncing: false,

    user: null,

  },

  //  ╦  ╦╔═╗╔═╗╔═╗╦ ╦╔═╗╦  ╔═╗
  //  ║  ║╠╣ ║╣ ║  ╚╦╝║  ║  ║╣
  //  ╩═╝╩╚  ╚═╝╚═╝ ╩ ╚═╝╩═╝╚═╝
  beforeMount: function() {
    //…
  },
  mounted: async function() {
    //…
  },

  //  ╦╔╗╔╔╦╗╔═╗╦═╗╔═╗╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
  //  ║║║║ ║ ║╣ ╠╦╝╠═╣║   ║ ║║ ║║║║╚═╗
  //  ╩╝╚╝ ╩ ╚═╝╩╚═╩ ╩╚═╝ ╩ ╩╚═╝╝╚╝╚═╝
  methods: {

    choosePieces: function (pieces) {

      this.formData = {
        ...this.formData,
        pieces,
      };

    },

    submittedForm: function ({match}) {

      const { matchID } = match;

      window.location = `/match/${matchID}`;

    },

    selectedUser: function (user) {

      const { username } = user;

      this.user = user;
      this.formData = {
        ...this.formData,
        username,
      };

    },

  },

});
